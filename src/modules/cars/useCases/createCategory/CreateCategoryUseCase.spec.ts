describe("Criar categoria", () => {
    it("test if 2 + 2 = 4", () => {
        const soma = 2 + 2;
        const result = 4;
        expect(soma).toBe(result);
    });

    it("test if 2 + 2 not 5", () => {
        const soma = 2 + 2;
        const resultado = 5;

        expect(soma).not.toBe(resultado);
    });
});
